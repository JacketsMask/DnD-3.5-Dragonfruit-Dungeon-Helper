    /**
     * Updates the visible character information on the main tab.
     */
    public void updateVisibleCharacterInformation() {
        CharacterBasicInfo info = player.getCharacterInfo();
        characterInfoNameLabel.setText("" + info.getName());
        characterClassLabel.setText(player.getClassInfo().getInitialClass().getName());
        characterInfoAgeLabel.setText("" + info.getAge());
        if (info.getOrder().equals(Order.NEUTRAL) && info.getAlignment().equals(Alignment.NEUTRAL)) {
            characterInfoAlignmentLabel.setText("True Neutral");
        } else {
            characterInfoAlignmentLabel.setText("" + info.getOrder() + " " + info.getAlignment());
        }
        characterInfoDeityLabel.setText("" + info.getDeity());
        characterInfoGenderLabel.setText("" + info.getGender());
        characterInfoRaceLabel.setText("" + info.getRace());
        characterInfoSizeLabel.setText("" + info.getSize());
        String height = "";
        if (info.getHeightFeet() != 0) {
            height += info.getHeightFeet() + " ft.";
        }
        if (info.getHeightInches() != 0) {
            height += " "+ info.getHeightInches() + " in.";
        }
        characterHeightLabel.setText(height);
        characterWeightLabel.setText("" + info.getWeight());
        characterEyesLabel.setText("" + info.getEyeColor());
        characterHairLabel.setText("" + info.getHairColor());
        characterSkinLabel.setText("" + info.getSkinColor());
    }
