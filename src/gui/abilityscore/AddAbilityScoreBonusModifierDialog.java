    private void saveModifierButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_saveModifierButtonActionPerformed
    {//GEN-HEADEREND:event_saveModifierButtonActionPerformed
        AbilityScore targetAbilityScore = AbilityScore.getAbilityScoreFromString((String) abilityScoreComboBox.getSelectedItem());
        String reason = reasonTextField.getText();
        int value = Integer.parseInt(valueTextField.getText());
        boolean permanent = permanentCheckBox.isSelected();
        int rounds;
        if (!permanent) {
            rounds = Integer.parseInt(roundsTextField.getText());
        } else {
            rounds = 0;
        }
        AbilityScoreEffect effect = new AbilityScoreEffect(targetAbilityScore, reason, EffectTarget.ABILITY_SCORE, value, permanent, rounds, false, false);
        //Activate the new effect
        player.getEffectManager().activate(effect);
        this.setVisible(false);
        parent.updateVisibleStatistics();
        AbilityScorePanel parent2 = parent.getParent();
        parent2.updateVisibleAbilityScoreBonus();
        parent2.updateVisibleAbilityScoreTotal();
        parent2.updateVisibleAbilityScoreModifiers();
    }//GEN-LAST:event_saveModifierButtonActionPerformed

    private void permanentCheckBoxActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_permanentCheckBoxActionPerformed
    {//GEN-HEADEREND:event_permanentCheckBoxActionPerformed
        boolean selected = permanentCheckBox.isSelected();
        if (selected) {
            roundsTextField.setEnabled(false);
        } else {
            roundsTextField.setEnabled(true);
        }
    }//GEN-LAST:event_permanentCheckBoxActionPerformed
