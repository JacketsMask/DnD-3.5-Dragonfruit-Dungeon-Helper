    private void receiveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_receiveButtonActionPerformed
        int platinum = Integer.parseInt(receievedPlatinum.getText());
        int gold = Integer.parseInt(receievedGold.getText());
        int silver = Integer.parseInt(receievedSilver.getText());
        int copper = Integer.parseInt(receievedCopper.getText());
        wallet.addMoney(platinum, gold, silver, copper);
        //Reset text
        receievedCopper.setText("0");
        receievedSilver.setText("0");
        receievedGold.setText("0");
        receievedPlatinum.setText("0");
        updateVisibleTotal();
        wallet.setSaved(false);
    }//GEN-LAST:event_receiveButtonActionPerformed

    private void purchaseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_purchaseButtonActionPerformed
        int platinum = Integer.parseInt(purchasePlatinum.getText());
        int gold = Integer.parseInt(purchaseGold.getText());
        int silver = Integer.parseInt(purchaseSilver.getText());
        int copper = Integer.parseInt(purchaseCopper.getText());
        wallet.subtractPurchaseCost(platinum, gold, silver, copper);
        //Reset text
        purchaseCopper.setText("0");
        purchaseSilver.setText("0");
        purchaseGold.setText("0");
        purchasePlatinum.setText("0");
        updateVisibleTotal();
        wallet.setSaved(false);
    }//GEN-LAST:event_purchaseButtonActionPerformed

