    private void walletTotalFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_walletTotalFocusLost
        JTextField field = (JTextField) evt.getSource();
        IntegerVerifier verifier = (IntegerVerifier) field.getInputVerifier();
        if (!verifier.isValueChanged()) {
            return;
        }
        if (field.equals(pouchPlatinum)) {
            wallet.setPlatinum(Integer.parseInt(pouchPlatinum.getText()));
        } else if (field.equals(pouchGold)) {
            wallet.setGold(Integer.parseInt(pouchGold.getText()));
        } else if (field.equals(pouchSilver)) {
            wallet.setSilver(Integer.parseInt(pouchSilver.getText()));
        } else if (field.equals(pouchCopper)) {
            wallet.setCopper(Integer.parseInt(pouchCopper.getText()));
        }
        updateVisibleTotal();
    }//GEN-LAST:event_walletTotalFocusLost

    /**
     * Updates the displayed wallet total from the wallet object.
     */
    private void updateVisibleTotal() {
        pouchCopper.setText("" + wallet.getCopper());
        pouchSilver.setText("" + wallet.getSilver());
        pouchGold.setText("" + wallet.getGold());
        pouchPlatinum.setText("" + wallet.getPlatinum());
        //Update visible weight
        double weight = wallet.getWeight();
        if (weight < 1) {
            weightLabel.setText("<html>Wallet weight: <b>" + weight + "</b> lb.</html>");
        } else {
            weightLabel.setText("<html>Wallet weight: <b>" + weight + "</b> lbs.</html>");
        }
    }
