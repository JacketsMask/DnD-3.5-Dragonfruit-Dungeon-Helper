    private void initializeArmorProficiencyList() {
        DefaultListModel<ArmorProficiency> model = new DefaultListModel<>();
        ArrayList<ArmorProficiency> profList = player.getProficiencies().getArmorProficiencyList();
        for (ArmorProficiency ap : profList) {
            model.addElement(ap);
        }
        armorProficiencyList.setModel(model);
    }

    private void initializeWeaponProficiencyList() {
        DefaultListModel<WeaponProficiency> model = new DefaultListModel<>();
        ArrayList<WeaponProficiency> profList = player.getProficiencies().getWeaponProficiencyList();
        for (WeaponProficiency ap : profList) {
            model.addElement(ap);
        }
        weaponProficiencyList.setModel(model);
    }

