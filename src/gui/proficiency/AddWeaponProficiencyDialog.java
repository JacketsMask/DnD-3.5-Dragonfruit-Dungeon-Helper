    private void learnProficiencyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_learnProficiencyButtonActionPerformed
        List selectedValuesList = null;
        if (evt.getSource() == simpleLearnProficiencyButton) {
            selectedValuesList = simpleProficiencyList.getSelectedValuesList();
        } else if (evt.getSource() == martialLearnProficiencyButton) {
            selectedValuesList = martialProficiencyList.getSelectedValuesList();
        } else if (evt.getSource() == exoticLearnProficiencyButton) {
            selectedValuesList = exoticProficiencyList.getSelectedValuesList();
        }
        for (Object o : selectedValuesList) {
            WeaponProficiency ap = (WeaponProficiency) o;
            character.getProficiencies().getWeaponProficiencyList().add(ap);
        }
        parent.refreshDisplayedInformation();
        this.dispose();
    }//GEN-LAST:event_learnProficiencyButtonActionPerformed

