    private void initializeProficiencyLists() {
        //Get the character weapon proficiencies
        ArrayList<WeaponProficiency> characterProfs = character.getProficiencies().getWeaponProficiencyList();
        //Create a list model to attach to the each unknown proficiency list
        DefaultListModel simpleModel = new DefaultListModel();
        DefaultListModel martialModel = new DefaultListModel();
        DefaultListModel exoticModel = new DefaultListModel();

        ArrayList<WeaponProficiency> unknownSimpleProfs = WeaponProficiency.getSimpleWeapons();
        ArrayList<WeaponProficiency> unknownMartialProfs = WeaponProficiency.getMartialWeapons();
        ArrayList<WeaponProficiency> unknownExoticProfs = WeaponProficiency.getExoticWeapons();
        System.out.println(unknownExoticProfs.size());
        //Remove each known proficiency from the unknown simple proficiency list
        for (WeaponProficiency wp : characterProfs) {
            unknownSimpleProfs.remove(wp);
            unknownMartialProfs.remove(wp);
            unknownExoticProfs.remove(wp);

        }
        //Transfer data to models
        for (WeaponProficiency wp : unknownSimpleProfs) {
            simpleModel.addElement(wp);
        }
        simpleProficiencyList.setModel(simpleModel);
        for (WeaponProficiency wp : unknownMartialProfs) {
            martialModel.addElement(wp);
        }
        martialProficiencyList.setModel(martialModel);
        for (WeaponProficiency wp : unknownExoticProfs) {
            exoticModel.addElement(wp);
        }
        exoticProficiencyList.setModel(exoticModel);
    }

