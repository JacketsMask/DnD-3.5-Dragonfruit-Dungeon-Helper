    private void selectAllButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectAllButtonActionPerformed
        JList list = null;
        if (evt.getSource() == simpleSelectAllButton) {
            list = simpleProficiencyList;
        } else if (evt.getSource() == martialSelectAllButton) {
            list = martialProficiencyList;
        } else if (evt.getSource() == exoticSelectAllButton) {
            list = exoticProficiencyList;
        }
        //Select all elements of the list
        int start = 0;
        DefaultListModel model = (DefaultListModel) list.getModel();
        int end = model.getSize() - 1;
        if (end >= 0) {
            list.setSelectionInterval(start, end);
        }
    }//GEN-LAST:event_selectAllButtonActionPerformed

    private void deselectAllButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deselectAllButtonActionPerformed
        if (evt.getSource() == simpleDeselectAllButton) {
            simpleProficiencyList.clearSelection();
        } else if (evt.getSource() == martialDeselectAllButton) {
            martialProficiencyList.clearSelection();
        } else if (evt.getSource() == exoticDeselectAllButton) {
            exoticProficiencyList.clearSelection();
        }
    }//GEN-LAST:event_deselectAllButtonActionPerformed

